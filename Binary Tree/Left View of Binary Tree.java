class Tree
{
    //Function to return list containing elements of left view of binary tree.
    ArrayList<Integer> leftView(Node root)
    {
        //list containing left most view
        ArrayList<Integer> ans = new ArrayList<>();
        if(root == null) {
            //if no node exists return empty list
            return ans;
        }
        
        //queue for processing
        Queue<Node> q = new ArrayDeque<>();
        //add root in queue
        q.add(root);
        
        //simple BFS(level order traversal)
        while(q.size()!=0){
            int count = q.size();
            
            //here queue's top is the left most node at that particular level
            ans.add(q.peek().data);
            while(count-->0){
                Node curr = q.remove();
                
                if(curr.left!=null){
                    q.add(curr.left);
                }
                if(curr.right!=null){
                    q.add(curr.right);
                }
            }
        }
        
        return ans;
    }
}

